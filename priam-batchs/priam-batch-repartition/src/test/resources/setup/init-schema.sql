CREATE TABLE SAREFFT_FAMILTYPUTIL (
  CDEFAMILTYPUTIL VARCHAR(10) NOT NULL,
  DATDBTVLD       DATE        NOT NULL,
  DATFINVLD       DATE,

  CONSTRAINT SAREFFT_FAMILTYPUTIL_PK PRIMARY KEY (CDEFAMILTYPUTIL)
